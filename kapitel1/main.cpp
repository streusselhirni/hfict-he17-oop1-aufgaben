// Binde über Präprozessor-Anweisung die Library iostream ein.
#include <iostream>

// Benutze den Namespace std
using namespace std;

// Das Programm startet immer in der main()-Funktion
int main() {
    // cout ist der Output-Stream. Ihm wird der Text und ein Zeilenumbruch übergeben.
    cout << "Hello World!" << endl;
    // Das main()-Programm muss einen Rückgabewert zurückgeben. 0 bedeutet "keine Fehler"
    return 0;
}
